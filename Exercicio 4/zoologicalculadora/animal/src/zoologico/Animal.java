package zoologico;

public class Animal {

 private String cor;
private int tamnho;
private int peso;
public String getCor() {
	return cor;
}

public void setCor(String cor) {
	this.cor = cor;
}

public int getTamnho() {
	return tamnho;
}

public void setTamnho(int tamnho) {
	this.tamnho = tamnho;
}

public int getPeso() {
	return peso;
}

public void setPeso(int peso) {
	this.peso = peso;
}

}

